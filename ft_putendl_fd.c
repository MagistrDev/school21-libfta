#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	char *str;

	if (s != NULL)
	{
		str = (char*)s;
		while (*str)
			write(1, str++, 1);
		write(fd, "\n", 1);
	}
}
